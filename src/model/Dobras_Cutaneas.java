public class Dobras_Cutaneas {
    private int id;
    private float peitoral, axilar_media, abdominal, coxa, bicipita,
            supra_iliaca, subscapular, tricipital;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public float getPeitoral() {
        return peitoral;
    }

    public void setPeitoral(float peitoral) {
        this.peitoral = peitoral;
    }

    public float getAxilar_media() {
        return axilar_media;
    }

    public void setAxilar_media(float axilar_media) {
        this.axilar_media = axilar_media;
    }

    public float getAbdominal() {
        return abdominal;
    }

    public void setAbdominal(float abdominal) {
        this.abdominal = abdominal;
    }

    public float getCoxa() {
        return coxa;
    }

    public void setCoxa(float coxa) {
        this.coxa = coxa;
    }

    public float getBicipita() {
        return bicipita;
    }

    public void setBicipita(float bicipita) {
        this.bicipita = bicipita;
    }

    public float getSupra_iliaca() {
        return supra_iliaca;
    }

    public void setSupra_iliaca(float supra_iliaca) {
        this.supra_iliaca = supra_iliaca;
    }

    public float getSubscapular() {
        return subscapular;
    }

    public void setSubscapular(float subscapular) {
        this.subscapular = subscapular;
    }

    public float getTricipital() {
        return tricipital;
    }

    public void setTricipital(float tricipital) {
        this.tricipital = tricipital;
    }
    
    
}
